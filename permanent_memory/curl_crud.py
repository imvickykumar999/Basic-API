# Post method
'''
curl -X POST https://vickscrudapi.pythonanywhere.com/post/ -d "name=John Doe" -d "email=johndoe@example.com"
'''

# Get method
'''
curl -X GET https://vickscrudapi.pythonanywhere.com/
'''

# Update method
'''
curl -X PUT https://vickscrudapi.pythonanywhere.com/update/1 -d "name=Jane Doe" -d "email=janedoe@example.com"
'''

# Get by ID method
'''
curl -X GET https://vickscrudapi.pythonanywhere.com/get/1
'''

# Patch method
'''
curl -X PATCH https://vickscrudapi.pythonanywhere.com/patch/1 -d "email=newemail@example.com"
'''
